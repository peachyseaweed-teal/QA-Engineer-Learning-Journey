# day2_ops.py
x = 10
y = 3

# arithmetic
print("x+y =", x + y)
print("x-y =", x - y)
print("x*y =", x * y)
print("x/y =", x / y) # float division
print("x//y", x // y) # integer division
print("x%y", x % y)  # remainder
print("x**y", x **y) # exponent

# comparison
print("x == y?", x == y)
print("x > y?", x > y)
print("x < y?", x < y)